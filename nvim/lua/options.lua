local opt = vim.opt

-- line numbers
opt.number = true

-- indentation (tab)
opt.tabstop = 4
opt.shiftwidth = 4
opt.expandtab = true

-- mouse support
opt.mouse = "a"

-- colors
opt.termguicolors = true

-- search
opt.ignorecase = true
opt.smartcase = true

-- don't create backup files
opt.swapfile = false
opt.backup = false
